function getNestedValue(tree: TranslationTree, path: string): string | TranslationTree | undefined {
  return path.split(".").reduce<string | TranslationTree | undefined>((current, part) => {
    if (!current || typeof current === "string") {
      return undefined;
    }
    return current[part];
  }, tree);
}

export function translate(language: Language, key: string, variables?: Record<string, string | number>): string {
  const resolved = getNestedValue(translations[language], key);
  const fallback = getNestedValue(translations[DEFAULT_LANGUAGE], key);
  const raw = typeof resolved === "string" ? resolved : typeof fallback === "string" ? fallback : key;

  if (!variables) {
    return raw;
  }

  return Object.entries(variables).reduce((text, [name, value]) => {
    return text.split(`{${name}}`).join(String(value));
  }, raw);
}
